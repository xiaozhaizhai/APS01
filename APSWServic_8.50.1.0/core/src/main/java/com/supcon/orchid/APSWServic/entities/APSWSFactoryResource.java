package com.supcon.orchid.APSWServic.entities;

public class APSWSFactoryResource {

    private String resCode;
    private String resName;
    private String resGroupCode;
    private String classCode;
    private String Differ;
    private String AvbResource;
    private String kind;

    public String getResCode() {
        return resCode;
    }

    public void setResCode(String resCode) {
        this.resCode = resCode;
    }

    public String getResName() {
        return resName;
    }

    public void setResName(String resName) {
        this.resName = resName;
    }

    public String getResGroupCode() {
        return resGroupCode;
    }

    public void setResGroupCode(String resGroupCode) {
        this.resGroupCode = resGroupCode;
    }

    public String getClassCode() {
        return classCode;
    }

    public void setClassCode(String classCode) {
        this.classCode = classCode;
    }

    public String getDiffer() {
        return Differ;
    }

    public void setDiffer(String differ) {
        Differ = differ;
    }

    public String getAvbResource() {
        return AvbResource;
    }

    public void setAvbResource(String avbResource) {
        AvbResource = avbResource;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }
}
